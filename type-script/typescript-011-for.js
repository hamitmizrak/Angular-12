//for
function forDongu(bitis) {
    var baslangic;
    for (baslangic = 0; baslangic <= bitis; baslangic++) {
        console.log(baslangic);
    }
}
forDongu(10);
//while
function whileDongu(bitis6) {
    var baslangic6 = 0;
    while (baslangic6 <= bitis6) {
        console.log(baslangic6);
        baslangic6++;
    }
}
whileDongu(10);
//dowhile
function doWhileDongu(bitis7) {
    var baslangic7 = 0;
    do {
        console.log("kesinlikle 1 kere çalışacak");
        baslangic7++;
    } while (baslangic7 <= bitis7);
}
doWhileDongu(9);

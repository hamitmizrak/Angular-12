function mathExamples(sayi) {
    console.log(Math.sqrt(sayi));
    console.log(Math.abs(sayi));
    console.log(Math.pow(2, sayi));
    console.log(Math.round(sayi));
    console.log(Math.ceil(sayi));
    console.log(Math.floor(sayi));
    console.log(Math.min(sayi, sayi * 2, sayi * -1));
    console.log(Math.max(sayi, sayi * 2, sayi * -1));
    console.log(Math.random() * 2 + 1);
}
mathExamples(16);
